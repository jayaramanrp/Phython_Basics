#Program using if..else statement 

print("Example for if..else statement")

if 2>3:
    print("2 is greater than 3")
else:
    print("2 is less than 3")

#Multi statement in if..else


if 5>3:
    print("we are inside if block")
    print("5 is greater than 3")
else:
    print("we are inside else block")
    print("5 is less than 3")
